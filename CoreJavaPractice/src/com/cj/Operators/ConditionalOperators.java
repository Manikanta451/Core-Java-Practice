package com.cj.Operators;

public class ConditionalOperators {

	public static void main(String[] args) {
		  int a, b;
	      a = 10;
	      b = (a == 10) ? 20: 30;
	      System.out.println( "Value of b is : " +  b );

	      b = (a == 18) ? 20: 50;
	      System.out.println( "Value of b is : " + b );

	}

}
